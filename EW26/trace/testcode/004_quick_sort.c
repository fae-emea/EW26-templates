static void QuickSort(int bottom, int top,unsigned long *data)
{
	unsigned long lower, upper, div, temp;

	if (bottom >= top) {
		return;
	}
	div = data[bottom];
	for (lower = bottom, upper = top; lower < upper;) {
		while (lower <= upper && data[lower] <= div) {
			lower++;
		}
		while (lower <= upper && data[upper] > div) {
			upper--;
		}
		if (lower < upper) {
			temp = data[lower];
			data[lower] = data[upper];
			data[upper] = temp;
		}
	}
	temp = data[bottom];
	data[bottom] = data[upper];
	data[upper] = temp;

	QuickSort(bottom, upper - 1, data);
	QuickSort(upper + 1, top, data);
}

